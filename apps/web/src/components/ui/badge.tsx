import * as React from 'react';
import { cva, type VariantProps } from 'class-variance-authority';
import { cn } from '@/lib/utils';

const badgeVariants = cva(
  'inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-slate-950 focus:ring-offset-2',
  {
    variants: {
      variant: {
        default:
          'border-transparent bg-slate-900 text-slate-50 hover:bg-slate-900/80',
        secondary:
          'border-transparent bg-slate-100 text-slate-900 hover:bg-slate-100/80',
        destructive:
          'border-transparent bg-red-500 text-slate-50 hover:bg-red-500/80',
        outline: 'text-slate-900 bg-white border-slate-300 hover:bg-slate-50',
        success:
          'border-transparent bg-green-500 text-white hover:bg-green-500/80',
        warning:
          'border-transparent bg-yellow-500 text-slate-900 hover:bg-yellow-500/80',
        info:
          'border-transparent bg-accent2 text-white hover:bg-accent2/80',
        draft:
          'border-transparent bg-slate-200 text-slate-700 hover:bg-slate-200/80',
        upcoming:
          'border-transparent bg-blue-100 text-blue-700 hover:bg-blue-100/80',
        live:
          'border-transparent bg-primary text-white hover:bg-primary/80',
        judging:
          'border-transparent bg-accent text-white hover:bg-accent/80',
        closed:
          'border-transparent bg-slate-400 text-white hover:bg-slate-400/80',
      },
    },
    defaultVariants: {
      variant: 'default',
    },
  }
);

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return (
    <div className={cn(badgeVariants({ variant }), className)} {...props} />
  );
}

export { Badge, badgeVariants };
